pub fn get_digit(c: char) -> Vec<&'static str> {
    match c {
        '0' => vec![
            " ##### ", "#     #", "#     #", "#     #", "#     #", "#     #", " ##### ",
        ],
        '1' => vec![
            "   #   ", "  ##   ", "   #   ", "   #   ", "   #   ", "   #   ", " ##### ",
        ],
        '2' => vec![
            " ##### ", "#     #", "      #", " ##### ", "#      ", "#      ", "#######",
        ],
        '3' => vec![
            " ##### ", "#     #", "      #", " ##### ", "      #", "#     #", " ##### ",
        ],
        '4' => vec![
            "#     #", "#     #", "#     #", "#######", "      #", "      #", "      #",
        ],
        '5' => vec![
            "#######", "#      ", "#      ", "#####  ", "     # ", "#    # ", " ####  ",
        ],
        '6' => vec![
            " ##### ", "#     #", "#      ", "###### ", "#     #", "#     #", " ##### ",
        ],
        '7' => vec![
            "#######", "#    # ", "    #  ", "   #   ", "  #    ", "  #    ", "  #    ",
        ],
        '8' => vec![
            " ##### ", "#     #", "#     #", " ##### ", "#     #", "#     #", " ##### ",
        ],
        '9' => vec![
            " ##### ", "#     #", "#     #", " ######", "      #", "#     #", " ##### ",
        ],
        ':' => vec![
            "       ", "   #   ", "   #   ", "       ", "   #   ", "   #   ", "       ",
        ],
        _ => vec![
            "       ", "       ", "       ", "       ", "       ", "       ", "       ",
        ],
    }
}
